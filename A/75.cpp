#include<iostream>
#include<cstdio>
#include<cstdlib> 
using namespace std;
int qu[4]={1,3,7,8},a[100010];
int main(){
	for(int i=1;i<=10000;i++){/*剩的球的个数*/ 
		for(int j=0;j<4;j++){
			if(i-qu[j]>0){/*首先保证有足够的球可以取*/ 
				if(a[i-qu[j]]==0){/*甲取了qu[i]个球之后该乙取,剩下的i-qu[i]个球乙先取*/ 
					a[i]=1;
				}
			}
		}
	}
	int n;
	cin>>n;
	int b;
	for(int i=0;i<n;i++){
		cin>>b;
		cout<<a[b]<<endl;
	} 
} 

